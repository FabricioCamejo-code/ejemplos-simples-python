import socket

host = '192.168.1.202'
port = 4444

obj = socket.socket()

obj.connect((host,port))

print("conectado al servidor")


while True:
    mens = eval(input("Mensaje desde cliente a servidor ".encode('utf-8')))
    obj.send(mens.encode('utf-8'))

    
    print("mensaje aceptado")
    datos = obj.recv(1024)
    print(datos.decode('utf-8'))
    print("enviado msj")

obj.close()
print("conexion termina")
