import socket
 

socket = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
socket.bind(("192.168.1.202", 4444))
socket.listen(1)
cli, addr = socket.accept()

while True:
    recibido = cli.recv(1024)
    print(recibido.decode('utf-8'))
    print ("Recibo conexion de la IP: " + str(addr[0]) + " Puerto: " + str(addr[1]))
    cli.send("mensaje recibido desde el servidor".encode('utf-8'))
    

    

cli.close()
socket.close()

